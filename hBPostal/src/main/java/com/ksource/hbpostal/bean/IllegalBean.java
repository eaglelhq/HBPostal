package com.ksource.hbpostal.bean;

public class IllegalBean {

	public String time;
	public boolean isDeal;
	public String addr;
	public String detail;
	public int money;
	public int fen;
	public IllegalBean(String time, boolean isDeal, String addr, String detail,
			int money, int fen) {
		super();
		this.time = time;
		this.isDeal = isDeal;
		this.addr = addr;
		this.detail = detail;
		this.money = money;
		this.fen = fen;
	}
	
	
}
