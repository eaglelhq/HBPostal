package com.yitao.db.entity;

public class ModelCache {
	private String id;
	private String ModelCache_Url;
	private String ModelCache_Data;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getModelCache_Url() {
		return ModelCache_Url;
	}

	public void setModelCache_Url(String modelCache_Url) {
		this.ModelCache_Url = modelCache_Url;
	}

	public String getModelCache_Data() {
		return ModelCache_Data;
	}

	public void setModelCache_Data(String modelCache_Data) {
		this.ModelCache_Data = modelCache_Data;
	}
}
